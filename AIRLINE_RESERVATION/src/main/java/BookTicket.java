    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        
        try {
            
            String Customer = CustomID.getText();
            
            Connection con;
            PreparedStatement pre;
            // Load MySQL JDBC Driver
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedatabase", "root", "Admin@1234");

            // Correct SQL Query
            pre = con.prepareStatement("SELECT * FROM Customer WHERE CustomerID=?");
            pre.setString(1, Customer);
            ResultSet rs = pre.executeQuery();
            if( rs.next() != false ){
                FirstN.setText(rs.getString("FirstName"));
                LastN.setText(rs.getString("LastName"));
                Cont.setText(rs.getString("Contact"));
                Genderr.setText(rs.getString("Gender"));
            }else {
                System.out.println("Customer Doesn't Exist!"); 
                JOptionPane.showMessageDialog(null, "Customer Doesn't Exist!");
            }
            // Close resources
            rs.close();
            pre.close();
            con.close();

        }catch(ClassNotFoundException | SQLException ex){
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SearchActionPerformed

