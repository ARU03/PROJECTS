    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        
        try {
            
            String FlightId = FlgID.getText();
            String FlightName = FlightN.getText();
            String Arrival = Arv.getText();
            String Departure = Depart.getText();
            String Duration = Durat.getText();
            String Seats = Seat.getText();
            String Fare = Faare.getText();
            DateFormat da = new SimpleDateFormat("yyyy-MM-dd");
            String DOFF = da.format(ChooseDOF.getDate());
            
            
            Connection con;
            PreparedStatement pre;

            // Load MySQL JDBC Driver
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedatabase", "root", "Admin@1234");
            pre = con.prepareStatement("INSERT INTO flight(FlightID,FlightName, Arrival,Departure, Duration, Seats, Fare, DOF)values(?,?,?,?,?,?,?,?)");

            pre.setString(1, FlightId);
            pre.setString(2, FlightName);
            pre.setString(3, Arrival);
            pre.setString(4, Departure);
            pre.setString(5, Duration);
            pre.setString(6, Seats);
            pre.setString(7, Fare);
            pre.setString(8, DOFF);
            
            pre.executeUpdate();
            
            System.out.println("Flight Added successfully!"); 
            JOptionPane.showMessageDialog(null, "Flight Added Successfully!");
            
        }catch(ClassNotFoundException | SQLException ex){
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_AddButtonActionPerformed


