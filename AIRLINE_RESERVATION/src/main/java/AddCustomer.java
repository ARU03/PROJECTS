    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        try {
            
            String CustomerId = CustID.getText();
            String FirstName = FirstN.getText();
            String LastName = LastN.getText();
            String PassportNo = PassN.getText();
            String NationalID = NatID.getText();
            String Address = ADDRS.getText();
            String Contact = Cont.getText();
            String Gender = "";
            if (Male.isSelected()){
                Gender = "Male";
            }else {
                Gender = "Female";
            }
            
            DateFormat da = new SimpleDateFormat("yyyy-MM-dd");
            String Date = da.format(ChooseDOB.getDate());
            
            
            Connection con;
            PreparedStatement pre;
            // Load MySQL JDBC Driver
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedatabase", "root", "Admin@1234");
            pre = con.prepareStatement("INSERT INTO customer(CustomerID,FirstName, LastName,PassportNo, NationalID, Address, Contact, Gender, Date)values(?,?,?,?,?,?,?,?,?)");
            
            // In place of question mark
            pre.setString(1, CustomerId);
            pre.setString(2, FirstName);
            pre.setString(3, LastName);
            pre.setString(4, PassportNo);
            pre.setString(5, NationalID);
            pre.setString(6, Address);
            pre.setString(7, Contact);
            pre.setString(8, Gender);
            pre.setString(9, Date);
            
            pre.executeUpdate();
            
            System.out.println("Customer added successfully!"); 
            JOptionPane.showMessageDialog(null, "Customer Added Successfully!");
            
            
            
        }
        catch(ClassNotFoundException | SQLException ex){
        Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_AddButtonActionPerformed


