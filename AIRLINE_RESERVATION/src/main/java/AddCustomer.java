    public void AutoID() throws SQLException {
    try {
        Connection con;
        PreparedStatement pre;

        // Load MySQL JDBC Driver
        Class.forName("com.mysql.cj.jdbc.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedatabase", "root", "Admin@1234");

        // Correct SQL Query
        pre = con.prepareStatement("SELECT MAX(CustomerID) FROM Customer");
        ResultSet rs = pre.executeQuery();
        rs.next();
            if (rs.getString("MAX(CustomerID)") == null) {
                CustID.setText("CS001"); // First customer ID
            } else {
                long id = Long.parseLong(rs.getString("MAX(CustomerID)").substring(2, rs.getString("MAX(CustomerID)").length() )); // Extract numeric part
                id++; // Increment ID
                CustID.setText("CS"+String.format("%03d", id)); // First customer ID

            }
        // Close resources
        rs.close();
        pre.close();
        con.close();

    }catch(ClassNotFoundException | SQLException ex){
        Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
    }
    }

